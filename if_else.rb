if 1 + 1 == 2 then
  puts "1 and 1 does indeed equal 2"
end

my_name = "Nate"
if my_name == "Nate" then
  puts "Hello, Nate!"
else
  puts "Sorry #{my_name}. This was an unfortunate case of mistaken identity."
end

fav_color = 'pink'
if (fav_color == 'red')
  puts "Red like Rudolph!"
elsif (fav_color == 'orange')
  puts "Orange like my favorite juice."
elsif (fav_color == 'yellow')
  puts "And the daffodils look lovely today."
elsif (fav_color == 'green')
  puts "Envy never looked so good"
elsif (fav_color == 'blue')
  puts "Superman chose it to blend in with the sky!"
elsif (fav_color == 'purple')
  puts "Fan of Professor Plum are we?"
else
  puts "Never heard of that particular color."
end
